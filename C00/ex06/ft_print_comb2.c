#include <unistd.h>

void	ft_print_comb2(void)
{
	int	i;
	int	j;

	i = 0;
	while (i <= 98)
	{
		j = i + 1;
		while (j <= 99)
		{
			write(1, (char [2]){i / 10 + '0', i % 10 + '0'}, 2);
			write(1, " ", 1);
			write(1, (char [2]){j / 10 + '0', j % 10 + '0'}, 2);
			if (!(i == 98 && j == 99))
				write(1, ", ", 2);
			j++;
		}
		i++;
	}
}
