#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c;

	if (nb < 0)
	{
		write(1, "-", sizeof(char));
		if (nb == -2147483648)
		{
			write(1, "2147483648", (10 * sizeof(char)));
			return ;
		}
		nb = -nb;
	}
	if (nb >= 10)
		ft_putnbr(nb / 10);
	c = nb % 10 + '0';
	write(1, &c, sizeof(char));
}
